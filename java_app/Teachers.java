    private void jButtoncreatesubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtoncreatesubjectActionPerformed
        // this button creates a new subject and inserts its corresponding data into the database 
        if (verifsubject()) 
        {

            Connection con = myConnection.getConnection() ;

            PreparedStatement ps;

            try {
                //ps = con.prepareStatement("INSERT INTO `user`(`fname`, `lname`, `username`, `pass`, `pic`) VALUES (?,?,?,?,?)") ;
                ps = con.prepareStatement("INSERT INTO subjects(subjectname, subjectcode, teacheruser ) VALUES (?,?,?)") ;
                ps.setString(1, jTextFieldsubjectname.getText());
                ps.setString(2, jTextFieldsubjectcode.getText());
                ps.setString(3, jLabeluserteach.getText());

                
                if (issubjectExist(jTextFieldsubjectname.getText()) ){
                    JOptionPane.showMessageDialog( null , " Subject already exists ");
                }else {
                    if (ps.executeUpdate() != 0) {
                        JOptionPane.showMessageDialog( null , " Subject created ");
                        Teachers supF = new Teachers() ;
                        supF.setVisible(true);
                        supF.pack();
                        supF.setLocationRelativeTo(null)  ;
                        supF.setDefaultCloseOperation(loginform.EXIT_ON_CLOSE);
                        supF.jLabelsubjectname.setText(jTextFieldsubjectname.getText());
                        supF.jLabeluserteach.setText(jLabelusernameteach.getText());
                        this.dispose();
                    }  else {
                        JOptionPane.showMessageDialog( null , " Something went wrong ");

                    }
                }

            } catch (Exception ex) {
                Logger.getLogger(signupform.class.getName()).log(Level.SEVERE, null, ex);

            }
        }
    }//GEN-LAST:event_jButtoncreatesubjectActionPerformed
   
     // this is the array list for the subjects created by the teacher    
    public ArrayList<subject> getSubjectsList()
    {
        ArrayList<subject> subjectsList = new ArrayList<subject> ();
        Connection con = myConnection.getConnection();  
        String query = "SELECT * FROM `subjects` where teacheruser = '"+jLabeluserteach.getText()+"' " ;
        Statement st ; 
        ResultSet rs; 
        try{
          st = con.createStatement();
          rs = st.executeQuery(query);
          subject Subject; 
          while(rs.next()) {
              
              Subject = new subject(rs.getString("subjectname"), rs.getString("subjectcode"), rs.getInt("id")) ;
              subjectsList.add(Subject);
          }
        } catch (Exception e){  
            e.printStackTrace();
        }
        return subjectsList;
    }   
    
    // this is the array list for the tasks created by the teacher for their specific subjects
    public ArrayList<task> getTasksList() {
        
        ArrayList<task> taskslist = new ArrayList<task> ();
        Connection con = myConnection.getConnection();  
        String query = "SELECT * FROM `tasks` where teacher = '"+jLabeluserteach.getText()+"' " ;
        Statement st ; 
        ResultSet rs;   
            try {
                st = con.createStatement();
                rs = st.executeQuery(query);  
                task Task;
            // while resultset returns true, a new object for task is created
            while(rs.next()) {
               Task = new task( rs.getString("tasktitle"), rs.getString("task") , rs.getInt("id") , rs.getString("task_subject") ,rs.getString("teacher") ,rs.getBytes("taskpic")  ) ;
                
              taskslist.add(Task);
          }
        } catch (Exception e){  
            e.printStackTrace();
        }
        return taskslist;
    }   
        
    
