    private void jButtoncreatetaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtoncreatetaskActionPerformed
    
        // this button creates the tasks and inserts all of the info about the tasks into the database 
        
            if (verifytaskData())
                  {
            
                
                    Connection con = myConnection.getConnection() ;
                   
                    
                    PreparedStatement ps;
                    
                    try {
                     
                        
                    ps = con.prepareStatement("INSERT INTO `tasks`(tasktitle,task,taskpic,task_subject,teacher) VALUES (?,?,?,?,?)") ;
                    ps.setString(1,jTasktitle.getText());
                    ps.setString(2,jTask.getText());
                    InputStream img = new FileInputStream(new File(imagePth));
                    ps.setBlob(3,img);
                    ps.setString(4, jCombotasksubject.getSelectedItem().toString());
                    ps.setString(5, jLabeluserteach.getText());
                    
                         
                   
                    if (ps.executeUpdate() != 0) {
                  JOptionPane.showMessageDialog( null , " Task created ");
                  }      else {
                  JOptionPane.showMessageDialog( null , " Something went wrong ");
                              }
                       
                    
                } catch (SQLException ex) {
                    Logger.getLogger(Teachers.class.getName()).log(Level.SEVERE, null, ex);
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(Teachers.class.getName()).log(Level.SEVERE, null, ex);
                }
        }
    }//GEN-LAST:event_jButtoncreatetaskActionPerformed

    private void jButtonBrowseImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBrowseImageActionPerformed
       //button for when an image is browsed on the computer 
       
       JFileChooser filec = new JFileChooser();
        filec.setCurrentDirectory(new File(System.getProperty("user.home")));

        // file extension

        FileNameExtensionFilter fileFilter = new FileNameExtensionFilter("*.jpg","png","gif");
        filec.addChoosableFileFilter(fileFilter);

        int fileState = filec.showSaveDialog(null);

        // if the user selects a file
        if(fileState == JFileChooser.APPROVE_OPTION){

            File selectedFile = filec.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            imagePth = path ;
            // display the image in the label using resize image

            jTaskpic.setIcon(resizePic(path));

            jTaskpic.setIcon(new ImageIcon(path));

        }
        // if the user cancels
        else if (fileState == JFileChooser.CANCEL_OPTION){
            System.out.println(" No Image Selected ") ;

        }
       
        
    

    }//GEN-LAST:event_jButtonBrowseImageActionPerformed
