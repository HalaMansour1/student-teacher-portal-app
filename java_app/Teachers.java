    // calculate grade when perceentage is entered 
   public void CalcGrade() {
 
                 percentage = jTextFieldPercentage.getText();
                 percent = Integer.parseInt(percentage);
                 
                
                 if (percent < 28 ){
                       jTextFieldGrade.setText("Grade 1");   
                 }  else if (percent > 28 && percent < 35) {
                       jTextFieldGrade.setText("Grade 2");
                 }
                    else if (percent > 35 && percent < 43) {
                       jTextFieldGrade.setText("Grade 3");
                 }  else if (percent > 43 && percent < 54) {   
                       jTextFieldGrade.setText("Grade 4");
                 }  else if (percent > 54 && percent < 66) {   
                       jTextFieldGrade.setText("Grade 5");
                 }  else if (percent > 66 && percent < 75) {   
                       jTextFieldGrade.setText("Grade 6");
                     
                 } else if (percent > 75 ) {
                    jTextFieldGrade.setText("Grade 7"); 
                 }
                 
                 
        
   }         
    
